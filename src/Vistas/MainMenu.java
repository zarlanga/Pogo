    public void setConnStatus(boolean c) {
        habilitarBotones(c);
        if (c) {
            jLabelConn.setText("CONECTADO A SERVIDOR SQL");
            jLabelConn.setForeground(Color.GREEN);
        } else {
            jLabelConn.setText("NO CONECTADO A SERVIDOR SQL");
            jLabelConn.setForeground(Color.RED);
        }
    }
    
